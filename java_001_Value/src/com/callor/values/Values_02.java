package com.callor.values;

public class Values_02 {

	public static void main(String[] args) {
		
		System.out.println("반지름이 23인 원의 둘레와 넓이를 계산하여 출력");
		System.out.print(23 * 2 * 3.14);
		System.out.println("(원의둘레) = 23 * 2 * 3.14");
		System.out.print(23 * 23 * 3.14);
		System.out.println("(원의넓이) = 23 * 23 * 3.14");
		
	}
	
}
